/*

변수 선언 및 초기화 
    자료형 변수명 = 값
변수명 규칙 
    알파벳,숫자, 언더바(_)로만 구성
    숫자로 시작(X)
    공백 포함(X)
    키워드(명령어) 변수명(X)

    !주의 - C언어는 알파벳 대/소문자 구분한다
*/

#include <stdio.h>

int main(void)
{
    int number1;    // int형 변수 선언
    int number2;

    number1 = 1;    // number1 변수에 1 값 대입
    number2 = 2;    // 초기화 
    

    int number3, number4;   // 두 개의 int형 변수 한번에 선언
    int number5 = 5;        // 선언 동시에 초기화(값 대입)



}







